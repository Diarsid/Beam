package diarsid.beam.core.base.util;

/**
 *
 * @author Diarsid
 */
public class BooleanUtils {
    
    public static boolean areSame(boolean b1, boolean b2) {
        return b1 == b2;
    }
    
    public static boolean areDifferent(boolean b1, boolean b2) {
        return b1 != b2;
    }
}
